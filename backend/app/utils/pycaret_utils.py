def detect_ml_task(df: pd.DataFrame, target_col: str = None) -> Tuple[str, str]:
    """
    Automatically detect the ML task type based on dataset characteristics.
    
    Args:
        df: Input DataFrame
        target_col: Optional target column name. If None, uses last column.
    
    Returns:
        Tuple of (task_type, target_column_name)
        task_type: 'classification', 'regression', 'clustering', 'anomaly', 'time_series'
    """
    # If no target specified, use last column
    if target_col is None:
        target_col = df.columns[-1]
    
    # Check if target column exists
    if target_col not in df.columns:
        return 'clustering', None
    
    y = df[target_col]
    
    # Check for time series patterns
    if pd.api.types.is_datetime64_any_dtype(y) or 'date' in target_col.lower() or 'time' in target_col.lower():
        return 'time_series', target_col
    
    # Check for numeric vs categorical
    if pd.api.types.is_numeric_dtype(y):
        # Check if float with true continuous values
        is_float = False
        if pd.api.types.is_float_dtype(y):
            is_float = not y.dropna().apply(lambda x: x == int(x) if pd.notna(x) else True).all()
            
        if is_float:
            return 'regression', target_col
            
        unique_ratio = y.nunique() / len(y) if len(y) > 0 else 0
        
        if len(y) < 50:
            if y.nunique() < 10:
                return 'classification', target_col
            else:
                return 'regression', target_col
        elif y.nunique() < 20 or unique_ratio < 0.05:
            return 'classification', target_col
        else:
            return 'regression', target_col
    
    # Categorical target
    elif pd.api.types.is_object_dtype(y) or pd.api.types.is_categorical_dtype(y):
        return 'classification', target_col
    
    return 'clustering', None


