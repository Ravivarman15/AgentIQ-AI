def detect_industry(df: pd.DataFrame) -> str:
    """
    Detect the industry domain based on column names and patterns.
    
    Args:
        df: Input DataFrame
    
    Returns:
        Industry name: 'Healthcare', 'Finance', 'Retail', 'Manufacturing', 'General'
    """
    col_names = [c.lower() for c in df.columns]
    col_str = ' '.join(col_names)
    
    # Healthcare indicators
    healthcare_keywords = ['patient', 'diagnosis', 'health', 'clinical', 'medical', 'disease', 
                          'treatment', 'hospital', 'doctor', 'symptom', 'bmi', 'blood', 'heart']
    if any(keyword in col_str for keyword in healthcare_keywords):
        return 'Healthcare'
    
    # Finance indicators
    finance_keywords = ['transaction', 'revenue', 'fraud', 'risk', 'finance', 'payment', 
                       'credit', 'debit', 'account', 'balance', 'loan', 'investment']
    if any(keyword in col_str for keyword in finance_keywords):
        return 'Finance'
    
    # Retail indicators
    retail_keywords = ['customer', 'sales', 'inventory', 'retail', 'product', 'price', 
                      'purchase', 'order', 'stock', 'category', 'sentiment']
    if any(keyword in col_str for keyword in retail_keywords):
        return 'Retail'
    
    # Manufacturing indicators
    manufacturing_keywords = ['production', 'quality', 'defect', 'machine', 'maintenance', 
                             'downtime', 'efficiency', 'manufacturing', 'assembly']
    if any(keyword in col_str for keyword in manufacturing_keywords):
        return 'Manufacturing'
    
    return 'General'


