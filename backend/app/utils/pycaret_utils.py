def detect_irrelevant_columns(df: pd.DataFrame, target_col: str = None) -> Dict[str, List[Dict[str, str]]]:
    """
    Automatically detect and classify irrelevant columns that should NOT be used as features.
    
    Detection Rules:
    1. ID-like columns (name contains 'id', 'uuid', 'code', 'key', 'index', 'serial')
    2. High cardinality columns (unique ratio > 95% of row count)
    3. Monotonic increasing sequences (likely auto-incremented indices)
    4. Constant columns (zero variance — only 1 unique value)
    5. Near-zero variance columns (one dominant value > 99%)
    
    Args:
        df: Input DataFrame
        target_col: Target column to exclude from removal
    
    Returns:
        Dict with 'columns_to_remove' list of {'column': str, 'reason': str}
        and 'columns_to_keep' list
    """
    columns_to_remove = []
    n_rows = len(df)
    
    if n_rows == 0:
        return {"columns_to_remove": [], "columns_to_keep": list(df.columns)}
    
    for col in df.columns:
        # Never remove target column
        if col == target_col:
            continue
        
        reason = None
        
        # --- Rule 1: ID-like column names ---
        col_lower = col.lower().strip()
        id_patterns = [
            r'^id$', r'_id$', r'^id_', r'_id_',
            r'uuid', r'guid',
            r'^index$', r'^idx$', r'^row_?num',
            r'serial_?n', r'^sn$', r'^s\.?no',
            r'^unnamed', r'^unnamed:',
            r'_key$', r'^key$',
            r'_code$', r'^code$',
            r'^record_?id', r'^entry_?id',
            r'transaction_?id', r'patient_?id', r'customer_?id',
            r'employee_?id', r'user_?id', r'order_?id',
            r'product_?id', r'item_?id', r'account_?id',
        ]
        
        for pattern in id_patterns:
            if re.search(pattern, col_lower):
                reason = f"ID-like column name matches pattern '{pattern}'"
                break
        
        if reason is None:
            series = df[col].dropna()
            n_valid = len(series)
            
            if n_valid == 0:
                reason = "Column is entirely null/empty"
            else:
                unique_count = series.nunique()
                unique_ratio = unique_count / n_valid if n_valid > 0 else 0
                
                # --- Rule 2: Constant columns (zero variance) ---
                if unique_count <= 1:
                    reason = f"Constant column (only {unique_count} unique value)"
                
                # --- Rule 3: Near-zero variance ---
                elif unique_count == 2:
                    # Check if one value dominates > 99%
                    value_counts = series.value_counts(normalize=True)
                    if value_counts.iloc[0] > 0.99:
                        reason = f"Near-zero variance: dominant value covers {value_counts.iloc[0]*100:.1f}% of data"
                
                # --- Rule 4: High cardinality (likely identifier) ---
                elif unique_ratio > 0.95 and n_valid > 20:
                    # Check if it's numeric and could be a meaningful feature
                    if pd.api.types.is_numeric_dtype(series):
                        # Only flag numeric columns as IDs if they are INTEGER-typed
                        # Continuous float columns (income, temperature, etc.) naturally have high uniqueness
                        is_integer_like = pd.api.types.is_integer_dtype(series) or (
                            pd.api.types.is_float_dtype(series) and
                            series.dropna().apply(lambda x: x == int(x)).all()
                        )
                        
                        if is_integer_like:
                            # Check if it's monotonically increasing (auto-increment ID)
                            if _is_monotonic_sequence(series):
                                reason = f"Monotonic increasing numeric sequence (likely auto-generated index)"
                            elif unique_ratio > 0.99:
                                reason = f"Extremely high cardinality integer column ({unique_count}/{n_valid} unique, {unique_ratio*100:.1f}%) — likely identifier"
                        # Float columns with continuous distributions are genuine features, do NOT remove
                    elif pd.api.types.is_object_dtype(series):
                        # String columns with near-unique values are almost always IDs
                        if unique_ratio > 0.95:
                            reason = f"High cardinality string column ({unique_count}/{n_valid} unique, {unique_ratio*100:.1f}%) — likely identifier"
                
                # --- Rule 5: Monotonic numeric sequences ---
                elif pd.api.types.is_numeric_dtype(series) and _is_monotonic_sequence(series):
                    if unique_ratio > 0.8:
                        reason = f"Monotonic increasing sequence with high uniqueness — likely index column"
        
        if reason:
            columns_to_remove.append({"column": col, "reason": reason})
    
    removed_col_names = {item["column"] for item in columns_to_remove}
    columns_to_keep = [col for col in df.columns if col not in removed_col_names and col != target_col]
    
    return {
        "columns_to_remove": columns_to_remove,
        "columns_to_keep": columns_to_keep
    }


def _is_monotonic_sequence(series: pd.Series) -> bool:
    """Check if a numeric series is approximately monotonically increasing/decreasing."""
    if not pd.api.types.is_numeric_dtype(series):
        return False
    
    clean = series.dropna().reset_index(drop=True)
    if len(clean) < 10:
        return False
    
    # Sample for performance on large datasets
    if len(clean) > 1000:
        clean = clean.iloc[::len(clean)//500]
    
    diffs = clean.diff().dropna()
    if len(diffs) == 0:
        return False
    
    # More than 95% of diffs are positive (increasing)
    positive_ratio = (diffs > 0).sum() / len(diffs)
    # Or more than 95% are negative (decreasing)
    negative_ratio = (diffs < 0).sum() / len(diffs)
    
    return positive_ratio > 0.95 or negative_ratio > 0.95


def advanced_feature_selection(
    df: pd.DataFrame,
    target_col: str,
    max_features: int = 50,
    correlation_threshold: float = 0.95,
    min_importance_percentile: float = 10.0
) -> Dict[str, Any]:
    """
    Senior-level feature selection pipeline:
    1. Remove already-identified irrelevant columns
    2. Correlation threshold filtering (remove highly correlated pairs)
    3. Mutual information ranking
    4. Tree-based feature importance
    5. Return clean, non-redundant, predictive feature set
    
    Args:
        df: Input DataFrame (already cleaned of ID columns)
        target_col: Target column name
        max_features: Maximum number of features to keep
        correlation_threshold: Threshold above which correlated features are removed
        min_importance_percentile: Minimum importance percentile to keep
    
    Returns:
        Dict with selected_features, removed_features, selection_report
    """
    if target_col not in df.columns:
        return {
            "selected_features": [c for c in df.columns if c != target_col],
            "removed_features": [],
            "selection_report": ["Target column not found — skipping feature selection."]
        }
    
    report = []
    features = [c for c in df.columns if c != target_col]
    original_count = len(features)
    removed_features = {}
    
    # Get numeric features for analysis
    numeric_features = df[features].select_dtypes(include=['number']).columns.tolist()
    categorical_features = [f for f in features if f not in numeric_features]
    
    if len(numeric_features) < 2:
        report.append(f"Only {len(numeric_features)} numeric features found — skipping advanced selection.")
        return {
            "selected_features": features,
            "removed_features": [],
            "selection_report": report
        }
    
    # --- Step 1: Correlation Threshold Filtering ---
    try:
        corr_matrix = df[numeric_features].corr().abs()
        upper_tri = corr_matrix.where(np.triu(np.ones(corr_matrix.shape), k=1).astype(bool))
        
        corr_to_drop = set()
        for col in upper_tri.columns:
            high_corr = upper_tri.index[upper_tri[col] > correlation_threshold].tolist()
            for hc in high_corr:
                if hc not in corr_to_drop:
                    corr_to_drop.add(hc)
                    removed_features[hc] = f"Highly correlated (r > {correlation_threshold}) with {col}"
        
        if corr_to_drop:
            numeric_features = [f for f in numeric_features if f not in corr_to_drop]
            report.append(f"🔗 Removed {len(corr_to_drop)} features due to high correlation (r > {correlation_threshold}): {list(corr_to_drop)}")
    except Exception as e:
        report.append(f"⚠️ Correlation filtering skipped: {str(e)}")
    
    # --- Step 2: Tree-Based Feature Importance ---
    try:
        from sklearn.ensemble import RandomForestClassifier, RandomForestRegressor
        from sklearn.preprocessing import LabelEncoder
        
        X_num = df[numeric_features].fillna(df[numeric_features].median())
        y = df[target_col].copy()
        
        # Encode target if categorical
        if y.dtype == 'object' or y.dtype.name == 'category':
            le = LabelEncoder()
            y = le.fit_transform(y.astype(str))
            is_classification = True
        else:
            y = y.fillna(y.median())
            is_classification = y.nunique() < 20
        
        if is_classification:
            rf = RandomForestClassifier(n_estimators=50, max_depth=8, random_state=42, n_jobs=-1)
        else:
            rf = RandomForestRegressor(n_estimators=50, max_depth=8, random_state=42, n_jobs=-1)
        
        rf.fit(X_num, y)
        importances = pd.Series(rf.feature_importances_, index=numeric_features).sort_values(ascending=False)
        
        # Remove features below minimum importance percentile
        if len(importances) > 5:
            threshold = np.percentile(importances.values, min_importance_percentile)
            low_importance = importances[importances < threshold].index.tolist()
            
            # SAFEGUARD: Never remove ALL features
            remaining = [f for f in numeric_features if f not in low_importance]
            if len(remaining) == 0 and len(low_importance) > 0:
                report.append(f"⚠️ Tree importance would remove all features — skipping to preserve features")
            else:
                for feat in low_importance:
                    if feat not in removed_features:
                        removed_features[feat] = f"Low tree-based importance ({importances[feat]:.4f} < threshold {threshold:.4f})"
                
                numeric_features = remaining
                if low_importance:
                    report.append(f"🌲 Removed {len(low_importance)} low-importance features via Random Forest analysis")
        
        report.append(f"📊 Top features by importance: {dict(importances.head(5).round(4))}")
        
    except Exception as e:
        report.append(f"⚠️ Tree-based importance skipped: {str(e)}")
    
    # --- Step 3: Mutual Information Ranking ---
    try:
        from sklearn.feature_selection import mutual_info_classif, mutual_info_regression
        
        X_mi = df[numeric_features].fillna(df[numeric_features].median())
        y_mi = df[target_col].copy()
        
        if y_mi.dtype == 'object' or y_mi.dtype.name == 'category':
            le = LabelEncoder()
            y_mi = le.fit_transform(y_mi.astype(str))
            mi_scores = mutual_info_classif(X_mi, y_mi, random_state=42)
        else:
            y_mi = y_mi.fillna(y_mi.median())
            mi_scores = mutual_info_regression(X_mi, y_mi, random_state=42)
        
        mi_series = pd.Series(mi_scores, index=numeric_features).sort_values(ascending=False)
        
        # Remove features with zero mutual information
        zero_mi = mi_series[mi_series == 0].index.tolist()
        
        # SAFEGUARD: Never remove ALL features via MI
        remaining_after_mi = [f for f in numeric_features if f not in zero_mi]
        if len(remaining_after_mi) == 0 and len(zero_mi) > 0:
            report.append(f"⚠️ MI would remove all {len(zero_mi)} features — skipping MI filtering to preserve features")
        else:
            for feat in zero_mi:
                if feat not in removed_features:
                    removed_features[feat] = "Zero mutual information with target"
            
            if zero_mi:
                numeric_features = remaining_after_mi
                report.append(f"📉 Removed {len(zero_mi)} features with zero mutual information")
        
    except Exception as e:
        report.append(f"⚠️ Mutual information ranking skipped: {str(e)}")
    
    # --- Combine final feature set ---
    final_features = numeric_features + categorical_features
    
    # SAFEGUARD: If everything was removed, fall back to original features
    if len(final_features) == 0:
        final_features = features  # Restore original feature list
        removed_features.clear()
        report.append(f"⚠️ All features were removed by selection — reverting to original {len(final_features)} features")
    
    # Cap total features
    if len(final_features) > max_features:
        final_features = final_features[:max_features]
        report.append(f"✂️ Capped features at {max_features} (from {len(final_features)})")
    
    report.insert(0, f"🎯 Feature selection: {original_count} → {len(final_features)} features retained")
    
    return {
        "selected_features": final_features,
        "removed_features": removed_features,
        "selection_report": report
    }


def clean_dataset_for_training(
    df: pd.DataFrame,
    target_col: str = None,
    manual_features: List[str] = None,
    auto_mode: bool = True
) -> Dict[str, Any]:
    """
    Complete dataset cleaning pipeline for ML training.
    
    This is the MAIN entry point that combines:
    1. Irrelevant column detection and removal
    2. Advanced feature selection (if auto mode)
    3. Manual feature validation (if manual mode)
    
    Args:
        df: Raw input DataFrame
        target_col: Target column (None = auto-detect using last column)
        manual_features: User-selected features (if manual mode)
        auto_mode: True = automatic feature selection, False = manual
    
    Returns:
        Dict with cleaned_df, target_col, features, cleaning_report, removed_columns
    """
    report = []
    removed_columns = []
    
    # Auto-detect target if not specified
    if target_col is None:
        target_col = df.columns[-1]
        report.append(f"🎯 Auto-detected target column: '{target_col}' (last column)")
    else:
        if target_col not in df.columns:
            return {
                "error": f"Target column '{target_col}' not found in dataset.",
                "available_columns": list(df.columns)
            }
        report.append(f"🎯 Using user-specified target column: '{target_col}'")
    
    if manual_features is not None and not auto_mode:
        # --- MANUAL MODE ---
        report.append("⚙️ Manual feature selection mode activated")
        
        # Validate manual features
        invalid_features = [f for f in manual_features if f not in df.columns]
        if invalid_features:
            report.append(f"⚠️ Invalid features removed: {invalid_features}")
            manual_features = [f for f in manual_features if f in df.columns]
        
        if target_col in manual_features:
            manual_features.remove(target_col)
            report.append(f"⚠️ Target column '{target_col}' removed from feature list")
        
        if len(manual_features) == 0:
            return {
                "error": "No valid features remaining after validation.",
                "available_columns": [c for c in df.columns if c != target_col]
            }
        
        features = manual_features
        cleaned_df = df[features + [target_col]].copy()
        report.append(f"✅ Using {len(features)} manually selected features")
        
    else:
        # --- AUTO MODE ---
        report.append("🤖 Automatic feature selection mode activated")
        
        # Step 1: Detect irrelevant columns
        detection = detect_irrelevant_columns(df, target_col)
        removed_columns = detection["columns_to_remove"]
        
        if removed_columns:
            removed_names = [item["column"] for item in removed_columns]
            report.append(f"🗑️ Removed {len(removed_columns)} irrelevant columns:")
            for item in removed_columns:
                report.append(f"   • {item['column']}: {item['reason']}")
            
            clean_df = df.drop(columns=removed_names, errors='ignore')
        else:
            clean_df = df.copy()
            report.append("✅ No irrelevant columns detected")
        
        # Step 2: Advanced feature selection
        selection = advanced_feature_selection(clean_df, target_col)
        features = selection["selected_features"]
        report.extend(selection["selection_report"])
        
        # Add removed features from selection step
        for feat, reason in selection["removed_features"].items():
            removed_columns.append({"column": feat, "reason": reason})
        
        # Build cleaned dataframe
        all_cols = features + [target_col]
        all_cols = [c for c in all_cols if c in clean_df.columns]
        cleaned_df = clean_df[all_cols].copy()
    
    report.append(f"📋 Final dataset: {cleaned_df.shape[0]} rows × {cleaned_df.shape[1]} columns")
    report.append(f"📋 Features: {len(features)} | Target: '{target_col}'")
    
    return {
        "cleaned_df": cleaned_df,
        "target_col": target_col,
        "features": features,
        "cleaning_report": report,
        "removed_columns": removed_columns
    }


