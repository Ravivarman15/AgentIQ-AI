def format_pycaret_metrics(metrics_df: pd.DataFrame, task_type: str) -> Dict[str, Any]:
    """
    Format PyCaret metrics DataFrame into a clean dictionary.
    
    Args:
        metrics_df: DataFrame from compare_models()
        task_type: The ML task type
    
    Returns:
        Formatted metrics dictionary
    """
    if metrics_df is None or metrics_df.empty:
        return {}
    
    # Get the best model (first row)
    best_model = metrics_df.iloc[0]
    
    formatted = {
        'model_name': best_model.get('Model', 'Unknown'),
        'metrics': {}
    }
    
    # Extract relevant metrics based on task type
    if task_type == 'classification':
        metric_keys = ['Accuracy', 'AUC', 'Recall', 'Prec.', 'F1', 'Kappa', 'MCC']
    elif task_type == 'regression':
        metric_keys = ['MAE', 'MSE', 'RMSE', 'R2', 'RMSLE', 'MAPE']
    else:
        metric_keys = list(best_model.index)
    
    for key in metric_keys:
        if key in best_model.index:
            value = best_model[key]
            if isinstance(value, (int, float)):
                formatted['metrics'][key] = round(float(value), 4)
            else:
                formatted['metrics'][key] = str(value)
    
    return formatted


def format_leaderboard(comparison_df: pd.DataFrame, task_type: str, top_n: int = 5) -> List[Dict[str, Any]]:
    """
    Format the model comparison leaderboard for professional display.
    
    Args:
        comparison_df: DataFrame from PyCaret compare_models + pull()
        task_type: ML task type
        top_n: Number of top models to include
    
    Returns:
        List of model dictionaries with standardized format
    """
    if comparison_df is None or comparison_df.empty:
        return []
    
    # Ensure 'Model' column exists
    try:
        if 'Model' not in comparison_df.columns:
            comparison_df = comparison_df.reset_index()
        if 'Model' not in comparison_df.columns:
            if 'index' in comparison_df.columns:
                comparison_df.rename(columns={'index': 'Model'}, inplace=True)
            elif comparison_df.shape[1] > 0:
                # Use first string column as Model
                for col in comparison_df.columns:
                    if comparison_df[col].dtype == 'object':
                        comparison_df.rename(columns={col: 'Model'}, inplace=True)
                        break
                else:
                    comparison_df.rename(columns={comparison_df.columns[0]: 'Model'}, inplace=True)
    except Exception:
        pass
    
    leaderboard = []
    
    if task_type == 'classification':
        display_metrics = ['Accuracy', 'AUC', 'Recall', 'Prec.', 'F1', 'Kappa']
    elif task_type == 'regression':
        display_metrics = ['MAE', 'MSE', 'RMSE', 'R2', 'RMSLE', 'MAPE']
    else:
        display_metrics = [c for c in comparison_df.columns if c != 'Model']
    
    for idx, row in comparison_df.head(top_n).iterrows():
        model_entry = {
            'rank': len(leaderboard) + 1,
            'Model': str(row.get('Model', f'Model_{idx}')),
        }
        
        for metric in display_metrics:
            if metric in row.index:
                val = row[metric]
                if isinstance(val, (int, float)):
                    model_entry[metric] = round(float(val), 4)
                else:
                    model_entry[metric] = str(val)
        
        # Add TT (Training Time) if available
        if 'TT (Sec)' in row.index:
            model_entry['Training_Time_Sec'] = round(float(row['TT (Sec)']), 2)
        
        leaderboard.append(model_entry)
    
    return leaderboard


