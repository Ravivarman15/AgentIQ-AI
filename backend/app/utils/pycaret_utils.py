def get_pycaret_setup_config(task_type: str, sample_mode: bool = True) -> Dict[str, Any]:
    """
    Get PyCaret setup configuration based on task type and mode.
    
    Senior-level config includes:
    - Proper cross-validation (5 folds for full, 3 for sample)
    - Normalization
    - Low variance removal
    - Multicollinearity handling
    - Class imbalance fixing (classification)
    - Outlier removal (full mode)
    
    Args:
        task_type: The ML task type
        sample_mode: Whether to use fast mode (less tuning)
    
    Returns:
        Dictionary of PyCaret setup parameters
    """
    base_config = {
        'session_id': 42,
        'verbose': False,
        'use_gpu': False,
        'fold': 3 if sample_mode else 5,
    }
    
    if task_type in ['classification', 'regression']:
        base_config.update({
            'normalize': True,
            'transformation': not sample_mode,
            'ignore_low_variance': True,
            'remove_multicollinearity': True,
            'multicollinearity_threshold': 0.9,
            'remove_outliers': not sample_mode,
        })
        
        if not sample_mode:
            base_config['outliers_threshold'] = 0.05
    
    elif task_type == 'clustering':
        base_config.update({
            'normalize': True,
            'transformation': not sample_mode,
            'ignore_low_variance': True,
        })
    
    elif task_type == 'anomaly':
        base_config.update({
            'normalize': True,
        })
    
    return base_config


