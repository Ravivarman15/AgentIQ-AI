def validate_python_version() -> Dict[str, Any]:
    """
    Validate Python version for PyCaret compatibility.
    PyCaret officially supports Python 3.9-3.11.
    
    Returns:
        Dict with 'compatible' (bool), 'version' (str), 'message' (str)
    """
    version = sys.version_info
    version_str = f"{version.major}.{version.minor}.{version.micro}"
    
    supported_versions = [(3, 9), (3, 10), (3, 11)]
    current = (version.major, version.minor)
    
    if current in supported_versions:
        return {
            "compatible": True,
            "version": version_str,
            "message": f"✅ Python {version_str} is fully compatible with PyCaret.",
            "recommendation": None
        }
    elif current == (3, 12):
        # 3.12 has partial support, allow but warn
        return {
            "compatible": True,
            "version": version_str,
            "message": f"⚠️ Python {version_str} has experimental PyCaret support. Recommended: 3.10 or 3.11.",
            "recommendation": "Consider using Python 3.10 (most stable) or 3.11 for full PyCaret compatibility."
        }
    else:
        return {
            "compatible": False,
            "version": version_str,
            "message": f"❌ Python {version_str} is NOT supported by PyCaret. Requires Python 3.9-3.11.",
            "recommendation": "Install Python 3.10 (most stable) or 3.11 for PyCaret compatibility."
        }


